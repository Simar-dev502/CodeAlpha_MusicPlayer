const appState = {
  currentAlbumIndex: 0,
  playbackAlbumIndex: 0,
  currentTrackIndex: 0,
  isShuffle: false,
  repeatMode: 'none',
  volume: 0.65
};

const audio = new Audio();
audio.volume = appState.volume;
window.player = { audio, appState };

const elements = {
  coverArt: document.getElementById('coverArt'),
  trackTitle: document.getElementById('trackTitle'),
  trackArtist: document.getElementById('trackArtist'),
  trackAlbum: document.getElementById('trackAlbum'),
  currentTime: document.getElementById('currentTime'),
  durationTime: document.getElementById('durationTime'),
  progressBar: document.getElementById('progressBar'),
  progressFill: document.getElementById('progressFill'),
  playBtn: document.getElementById('playBtn'),
  prevBtn: document.getElementById('prevBtn'),
  nextBtn: document.getElementById('nextBtn'),
  shuffleBtn: document.getElementById('shuffleBtn'),
  repeatBtn: document.getElementById('repeatBtn'),
  volumeDownBtn: document.getElementById('volumeDownBtn'),
  volumeUpBtn: document.getElementById('volumeUpBtn'),
  volumeDisplay: document.getElementById('volumeDisplay'),
  playlistContainer: document.getElementById('playlistContainer'),
  playlistTitle: document.getElementById('playlistTitle'),
  playlistCount: document.getElementById('playlistCount'),
  albumTabs: document.getElementById('albumTabs'),
  menuBtn: document.getElementById('menuBtn'),
  settingsBtn: document.getElementById('settingsBtn'),
  menuPanel: document.getElementById('menuPanel'),
  menuTitle: document.getElementById('menuTitle'),
  closeMenuBtn: document.getElementById('closeMenuBtn'),
  panelShuffleBtn: document.getElementById('panelShuffleBtn'),
  panelRepeatBtn: document.getElementById('panelRepeatBtn'),
  panelVolumeDownBtn: document.getElementById('panelVolumeDownBtn'),
  panelVolumeUpBtn: document.getElementById('panelVolumeUpBtn')
};

function getCurrentAlbum() {
  return albumData[appState.currentAlbumIndex];
}

function getCurrentTracks() {
  return getCurrentAlbum().tracks;
}

function getPlaybackAlbum() {
  return albumData[appState.playbackAlbumIndex];
}

function getPlaybackTracks() {
  return getPlaybackAlbum().tracks;
}

function formatTime(seconds) {
  const mins = Math.floor(seconds / 60) || 0;
  const secs = Math.floor(seconds % 60) || 0;
  return `${mins}:${secs.toString().padStart(2, '0')}`;
}

function loadTrack(index, albumIndex = appState.playbackAlbumIndex) {
  const album = albumData[albumIndex];
  const tracks = album.tracks;
  const track = tracks[index];
  if (!track) return;

  appState.playbackAlbumIndex = albumIndex;
  appState.currentTrackIndex = index;
  audio.src = track.src;
  audio.load();
  elements.trackTitle.textContent = track.title;
  elements.trackArtist.textContent = track.artist;
  elements.trackAlbum.textContent = `• ${album.name}`;

  if (album.cover) {
    elements.coverArt.innerHTML = `<img src="${album.cover}" alt="${track.title} cover">`;
  } else {
    elements.coverArt.innerHTML = '<i class="fas fa-music"></i>';
  }

  const activeIndex = appState.currentAlbumIndex === appState.playbackAlbumIndex ? index : -1;
  updateActivePlaylistItem(activeIndex);
  updatePlaylistHeader();
}

function playTrack() {
  audio.play().catch(() => {
    // Autoplay may be blocked until user interaction.
  });
  elements.playBtn.classList.replace('fa-play-circle', 'fa-pause-circle');
}

function pauseTrack() {
  audio.pause();
  elements.playBtn.classList.replace('fa-pause-circle', 'fa-play-circle');
}

function togglePlay() {
  if (audio.paused) {
    playTrack();
  } else {
    pauseTrack();
  }
}

function updateProgress() {
  const { currentTime, duration } = audio;
  if (!isNaN(duration) && duration > 0) {
    const progressPercent = (currentTime / duration) * 100;
    elements.progressFill.style.width = `${progressPercent}%`;
    elements.currentTime.textContent = formatTime(currentTime);
    elements.durationTime.textContent = formatTime(duration);
  }
}

function seek(event) {
  const rect = elements.progressBar.getBoundingClientRect();
  const clickX = event.clientX - rect.left;
  const width = rect.width;
  const seekTime = (clickX / width) * audio.duration;
  if (!isNaN(seekTime)) {
    audio.currentTime = seekTime;
  }
}

function playNext() {
  const tracks = getPlaybackTracks();
  if (appState.isShuffle) {
    playRandomTrack();
    return;
  }

  if (appState.currentTrackIndex >= tracks.length - 1) {
    if (appState.repeatMode === 'all') {
      loadTrack(0);
      playTrack();
    } else {
      pauseTrack();
    }
    return;
  }

  loadTrack(appState.currentTrackIndex + 1);
  playTrack();
}

function playPrevious() {
  if (audio.currentTime > 3) {
    audio.currentTime = 0;
    return;
  }

  const tracks = getPlaybackTracks();
  if (appState.currentTrackIndex <= 0) {
    if (appState.repeatMode === 'all') {
      loadTrack(tracks.length - 1);
      playTrack();
    } else {
      audio.currentTime = 0;
    }
    return;
  }

  loadTrack(appState.currentTrackIndex - 1);
  playTrack();
}

function playRandomTrack() {
  const tracks = getPlaybackTracks();
  let nextIndex = Math.floor(Math.random() * tracks.length);
  if (tracks.length > 1 && nextIndex === appState.currentTrackIndex) {
    nextIndex = (nextIndex + 1) % tracks.length;
  }
  loadTrack(nextIndex);
  playTrack();
}

function cycleRepeatMode() {
  if (appState.repeatMode === 'none') {
    appState.repeatMode = 'one';
  } else if (appState.repeatMode === 'one') {
    appState.repeatMode = 'all';
  } else {
    appState.repeatMode = 'none';
  }
  updateRepeatButton();
}

function updateRepeatButton() {
  elements.repeatBtn.classList.remove('active', 'repeat-one', 'repeat-all');
  if (appState.repeatMode === 'one') {
    elements.repeatBtn.classList.add('active', 'repeat-one');
  } else if (appState.repeatMode === 'all') {
    elements.repeatBtn.classList.add('active', 'repeat-all');
  }
}

function toggleShuffle() {
  appState.isShuffle = !appState.isShuffle;
  elements.shuffleBtn.classList.toggle('active', appState.isShuffle);
}

function updateActivePlaylistItem(activeIndex) {
  const items = document.querySelectorAll('.playlist-item');
  items.forEach((item, index) => {
    item.classList.toggle('active', index === activeIndex);
  });
}

function createPlaylistItem(track, index) {
  const item = document.createElement('div');
  item.className = 'playlist-item';
  item.dataset.index = index;
  item.innerHTML = `
    <div class="track-meta">
      <span class="track-title">${track.title}</span>
      <span class="track-subtitle">${track.artist}</span>
    </div>
    <span class="track-album">${getCurrentAlbum().name}</span>
  `;

  item.addEventListener('click', () => {
    loadTrack(index, appState.currentAlbumIndex);
    playTrack();
  });

  return item;
}

function renderPlaylist() {
  elements.playlistContainer.innerHTML = '';
  getCurrentTracks().forEach((track, index) => {
    const item = createPlaylistItem(track, index);
    elements.playlistContainer.appendChild(item);
  });
  updatePlaylistHeader();

  const activeIndex = appState.currentAlbumIndex === appState.playbackAlbumIndex ? appState.currentTrackIndex : -1;
  updateActivePlaylistItem(activeIndex);
}

function renderAlbumTabs() {
  elements.albumTabs.innerHTML = '';
  albumData.forEach((album, index) => {
    const tab = document.createElement('button');
    tab.className = 'tab-item';
    tab.textContent = album.name;
    tab.addEventListener('click', () => selectAlbum(index));
    if (index === appState.currentAlbumIndex) {
      tab.classList.add('active');
    }
    elements.albumTabs.appendChild(tab);
  });
}

function selectAlbum(index) {
  appState.currentAlbumIndex = index;
  renderAlbumTabs();
  renderPlaylist();
}

function updatePlaylistHeader() {
  const currentAlbum = getCurrentAlbum();
  const trackCount = getCurrentTracks().length;
  elements.playlistTitle.textContent = currentAlbum.name;
  elements.playlistCount.innerHTML = `<i class="fas fa-list-ul"></i> ${trackCount} tracks`;
}

function adjustVolume(step) {
  appState.volume = Math.min(1, Math.max(0, appState.volume + step));
  audio.volume = appState.volume;
  updateVolumeDisplay();
}

function updateVolumeDisplay() {
  elements.volumeDisplay.textContent = `${Math.round(appState.volume * 100)}%`;
}

function showPanel(title) {
  elements.menuTitle.textContent = title;
  elements.menuPanel.classList.remove('hidden');
}

function closePanel() {
  elements.menuPanel.classList.add('hidden');
}

function handleTrackEnd() {
  if (appState.repeatMode === 'one') {
    audio.currentTime = 0;
    playTrack();
    return;
  }
  playNext();
}

function init() {
  renderAlbumTabs();
  renderPlaylist();
  loadTrack(appState.currentTrackIndex);
  updateVolumeDisplay();
  updateRepeatButton();

  elements.playBtn.addEventListener('click', togglePlay);
  elements.prevBtn.addEventListener('click', playPrevious);
  elements.nextBtn.addEventListener('click', playNext);
  elements.shuffleBtn.addEventListener('click', toggleShuffle);
  elements.repeatBtn.addEventListener('click', cycleRepeatMode);
  elements.volumeDownBtn.addEventListener('click', () => adjustVolume(-0.1));
  elements.volumeUpBtn.addEventListener('click', () => adjustVolume(0.1));
  elements.progressBar.addEventListener('click', seek);
  elements.menuBtn.addEventListener('click', () => showPanel('Menu'));
  elements.settingsBtn.addEventListener('click', () => showPanel('Settings'));
  elements.closeMenuBtn.addEventListener('click', closePanel);
  elements.panelShuffleBtn.addEventListener('click', toggleShuffle);
  elements.panelRepeatBtn.addEventListener('click', cycleRepeatMode);
  elements.panelVolumeDownBtn.addEventListener('click', () => adjustVolume(-0.1));
  elements.panelVolumeUpBtn.addEventListener('click', () => adjustVolume(0.1));
  elements.menuPanel.addEventListener('click', (event) => {
    if (event.target === elements.menuPanel) closePanel();
  });

  audio.addEventListener('timeupdate', updateProgress);
  audio.addEventListener('loadedmetadata', updateProgress);
  audio.addEventListener('ended', handleTrackEnd);
}

init();
